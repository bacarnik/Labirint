function getLineCells(line) {
    const cells = [];
    const x1 = Math.floor(line.x1 / GRID_SIZE);
    const y1 = Math.floor(line.y1 / GRID_SIZE);
    const x2 = Math.floor(line.x2 / GRID_SIZE);
    const y2 = Math.floor(line.y2 / GRID_SIZE);
    
    const dx = Math.abs(x2 - x1), sx = x1 < x2 ? 1 : -1;
    const dy = -Math.abs(y2 - y1), sy = y1 < y2 ? 1 : -1;
    let err = dx + dy;
    
    let x = x1, y = y1;
    while (true) {
        cells.push(`${x},${y}`);
        if (x === x2 && y === y2) break;
        const e2 = 2 * err;
        if (e2 >= dy) { err += dy; x += sx; }
        if (e2 <= dx) { err += dx; y += sy; }
    }
    return cells;
}

